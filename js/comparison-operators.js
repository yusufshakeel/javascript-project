/**
 * comparison operators
 */

// is equal to
/*
var x = 10;
var y = 11;
console.log(x == y);
*/

// not equal to
/*
var x = 10;
var y = 10;
console.log(x != y);
*/

// is equal to and of same type
/*
var x = 10;
var y = "10";
console.log(typeof x);
console.log(typeof y);
console.log(x === y);
*/

// is not equal to or not of same type
/*
var x = 10;
var y = "10";
console.log(typeof x);
console.log(typeof y);
console.log(x !== y);
*/

// greater than
/*
var x = 10;
var y = 15;
console.log(x > y);
*/

// less than
/*
var x = 200;
var y = 100;
console.log(x < y);
*/

// greater than or equal to
/*
var x = 9;
var y = 10;
console.log(x >= y);
*/

// less than or equal to
var x = 11;
var y = 10;
console.log(x <= y);