/**
 * arithmetic operators
 *
 * addition +
 * multiplication *
 * subtraction -
 * division /
 * modulus %
 */

//addition
var x = 10;
var y = 20;

var result = x + y;
console.log(result);

//multiplication
var a = 4;
var b = 5;
console.log(a * b);

//subtraction
var m = 20.5;
var n = 30.5;
console.log(m - n);

//division
var u = 5.2;
var v = 5;
console.log(u / v);

//modulus
var c = 5;
var d = 2;
console.log(c % d);

/**
 * increment ++	we are adding 1
 * decrement -- we are subtracting 1
 */
var j = 10;
j++;			//j = j + 1
console.log(j);

var k = 10;
k--;			//k = k - 1
console.log(k);


